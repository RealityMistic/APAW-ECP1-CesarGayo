public class Agente {
    int idAgente;
    String nombreAgente;
    String telefonoAgente;

    private Agente(int idAgente, String nombreAgente, String telefonoAgente) {
        this.idAgente = idAgente;
        this.nombreAgente = nombreAgente;
        this.telefonoAgente = telefonoAgente;
    }
}
